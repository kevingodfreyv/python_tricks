# isalnum() method example
# The isalnum() method checks if all the characters in a string are alphanumeric (either letters or numbers).
print("a".isalnum())  # True    
print("A".isalnum())  # True
print("1".isalnum())  # True
print("hello".isalnum())  # True
print("Hello123".isalnum())  # True
print("hello world!".isalnum())  # False